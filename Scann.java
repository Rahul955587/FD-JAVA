import java.util.*;
public class Scann {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        
        byte a=sc.nextByte();       //For Byte
        short b=sc.nextShort();     //Short
        int c=sc.nextInt();         //Integers
        long d=sc.nextLong();       //Long
        float e=sc.nextFloat();     //Float 
        double f=sc.nextDouble();   //Double
        boolean g=sc.nextBoolean(); //Boolean
        char h=sc.next().charAt(0); //For any one character
        String i=sc.next();     //For only starting one Word
        String j=sc.nextLine(); //For full String or Sentence
        sc.close();


        
    }
}
